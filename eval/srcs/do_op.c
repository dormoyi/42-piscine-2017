int		do_op(int a, char b, int c)
{
	if (b == '+')
		return (a + c);
	if (b == '-')
		return (a - c);
	if (b == '*')
		return (a * c);
	if (b == '/')
		return (a / c);
	if (b == '%')
		return (a % c);
	return (0);
}
