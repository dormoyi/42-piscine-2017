char	*ft_strrev(char *str)
{
	int		i1;
	int		i2;
	char	a;

	i2 = 0;
	while (str[i2] != '\0')
	{
		i2++;
	}
	i2--;
	i1 = 0;
	while (i1 < i2)
	{
		a = str[i1];
		str[i1] = str[i2];
		str[i2] = a;
		i1++;
		i2--;
	}
	return (str);
}
