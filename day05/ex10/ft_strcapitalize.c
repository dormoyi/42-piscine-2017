char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			str[i] = str[i] - ('A' - 'a');
		i++;
	}
	i = 0;
	if (('a' <= str[0]) && (str[0] <= 'z'))
		str[0] = str[0] - ('a' - 'A');
	while (str[i] != '\0')
	{
		if (!(('a' <= str[i] && str[i] <= 'z') || ('A' <= str[i]
						&& str[i] <= 'Z') || ('0' <= str[i] && str[i] <= '9')))
		{
			if (('a' <= str[i + 1]) && (str[i + 1] <= 'z'))
				str[i + 1] = str[i + 1] - ('a' - 'A');
		}
		i++;
	}
	return (str);
}
