char	*ft_strcat(char *dest, char *src)
{
	int end;
	int i;

	end = 0;
	i = 0;
	while (dest[end] != '\0')
		end++;
	while (src[i] != '\0')
	{
		dest[end] = src[i];
		i++;
		end++;
	}
	dest[end] = '\0';
	return (dest);
}
