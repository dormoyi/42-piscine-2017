int		ft_atoi(char *str)
{
	int i;
	int sign;
	int nb;

	nb = 0;
	i = 0;
	sign = 1;
	while ((8 < str[i] && str[i] < 14) || str[i] == 32)
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (47 < str[i] && str[i] < 58)
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	if (nb > 0 && sign == -1)
		return (-nb);
	else
		return (nb);
}
