char	*ft_strcpy(char *dest, char *src)
{
	int len;
	int i;

	i = 0;
	len = 0;
	while (*(src + len) != '\0')
		len++;
	while (i <= len)
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}
